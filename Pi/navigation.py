import math
from typing import Tuple

def distance_between(p1, p2):
    return math.sqrt((p1[0] - p2[0])**2 + (p1[1] - p2[1])**2)

def calculate_angle_to(p1, p2):
    return math.degrees(math.atan2(p2[1] - p1[1], p2[0] - p1[0]))

def angle_difference(a1, a2):
    diff = (a2 - a1 + 180) % 360 - 180
    return diff

def current_position(prev_pos: Tuple[float, float], heading: float, speed: float, delta_t: float) -> Tuple[float, float]:
    """
    Estimates new position based on previous position, heading (in degrees), speed (units/sec), and loop interval (seconds).
    """
    rad = math.radians(heading)
    dx = math.cos(rad) * speed * delta_t
    dy = math.sin(rad) * speed * delta_t
    return (prev_pos[0] + dx, prev_pos[1] + dy)



